            <h4>Congreso Caleidoscopio: Una nueva proyección de las Relaciones Internacionales</h4>
            <div class="text">
                Caleidoscopio tiene como objetivo presentar la versatilidad de la disciplina de las
                Relaciones Internacionales mediante diferentes acercamientos y enfoques, descubriendo el
                amplio campo de desarrollo que existe. De tal forma se pretende demostrar la imposibilidad
                de encasillar a un internacionalista a un solo campo de trabajo, exponiendo opciones
                muchas veces desconocidas por los propios estudiantes que la disciplina en si representa.
                <br>
                Como internacionalistas, creemos que una de las cualidades fundamentales para poder
                desarrollarse integralmente es la capacidad de cooperar en conjunto para lograr un
                objetivo en común. Este Congreso parte de esa premisa al establecerse como un foco
                para el contacto, la interacción, las relaciones personales, pero sobre todo, el intercambio
                de conocimiento y recursos mediante la creación de una red de estudiantes, alentando
                la participación entre los mismos, así como el intercambio de ideas, propuestas y
                experiencias.
            </div>
